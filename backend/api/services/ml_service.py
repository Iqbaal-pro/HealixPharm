def get_trends() -> Dict[str, Any]:
    """
    Reads preprocessed_12_month_data.csv
    Returns monthly sales totals per category for line charts.

    Excludes 'Other Meds/Unclassified' (too noisy for trends).
    """
    _file_exists_or_raise(
        TRENDS_CSV,
        hint="Run POST /api/v1/predict/run first."
    )

    df = pd.read_csv(TRENDS_CSV)

    # Aggregate: total qty per month per category
    trend_df = (
        df.groupby(["Month", "Category"])["Qty"]
        .sum()
        .reset_index()
    )

    # Remove noisy unclassified category
    trend_df = trend_df[trend_df["Category"] != "Other Meds/Unclassified"]

    # Sort by month
    trend_df = trend_df.sort_values(["Category", "Month"])

    # Build date range string
    date_range = "Apr 2025 - Mar 2026"
    if "Date" in df.columns:
        try:
            dates = pd.to_datetime(df["Date"], errors="coerce")
            min_d = dates.min().strftime("%b %Y")
            max_d = dates.max().strftime("%b %Y")
            date_range = f"{min_d} - {max_d}"
        except Exception:
            pass

    categories = sorted(trend_df["Category"].unique().tolist())

    data_points = [
        {
            "month": int(row["Month"]),
            "month_name": MONTH_NAMES.get(int(row["Month"]), str(row["Month"])),
            "category": str(row["Category"]),
            "total_qty": round(float(row["Qty"]), 2),
        }
        for _, row in trend_df.iterrows()
    ]

    return {
        "date_range": date_range,
        "categories": categories,
        "data": data_points,
    }


