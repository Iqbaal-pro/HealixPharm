def _file_exists_or_raise(path: Path, hint: str = "") -> None:
    """
    Checks if a CSV file exists.
    If NOT → raises FileNotFoundError with a helpful message.
    This prevents cryptic pandas errors reaching the frontend.
    """
    if not path.exists():
        msg = f"File not found: {path.name}."
        if hint:
            msg += f" {hint}"
        raise FileNotFoundError(msg)


def _get_target_month_from_csv(csv_path: Path) -> str:
    """
    Reads the predicted quantity column name from the CSV
    to extract the target month (e.g. 'April 2026').
    Column looks like: 'Predicted_April_2026_Qty'
    """
    try:
        df = pd.read_csv(csv_path, nrows=0)  # read headers only
        pred_cols = [c for c in df.columns if c.startswith("Predicted_")]
        if pred_cols:
            # 'Predicted_April_2026_Qty' → 'April 2026'
            parts = pred_cols[0].replace("Predicted_", "").replace("_Qty", "")
            return parts.replace("_", " ")
    except Exception:
        pass
    return "Next Month"


